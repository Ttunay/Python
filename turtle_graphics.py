import turtle

turtle.showturtle()
turtle.forward(50) # еденицы указыватся в градусах
turtle.left(45)
turtle.forward(50)
turtle.left(45)
turtle.forward(50)
turtle.left(45)
turtle.forward(50)
turtle.penup() # поднять перо
turtle.forward(50)
turtle.pendown() # опустить перо
turtle.dot() # поставить точку
turtle.pensize(5) # ширина пера
turtle.bgcolor('red') # цвет фона
turtle.pencolor('green') # цвет пера
turtle.circle(100)
turtle.reset() # стирает все рисунки, черный цвет рисунка, задает черепаху в исходное положение
turtle.circle(100)